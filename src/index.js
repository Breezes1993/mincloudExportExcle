function plantDurationUnitCodeToStr(code) {
  if (code == 1) return "月";
  if (code == 2) return "年";
  return "天";
}
function getPlantDurationStr(plantDuration, plantDurationUnit) {
  if (!plantDuration && plantDuration !== 0) return "";
  return `${plantDuration} ${plantDurationUnitCodeToStr(plantDurationUnit)}`;
}
